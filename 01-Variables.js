// Vamos a ver como declarar variables en JavaScript.

//1er método: var. Es la forma más antigua de declarar variables en JavaScript. No se recomienda su uso.
var nombre = "Juan"
console.log(nombre)

//2do método: let. Es la forma más moderna de declarar variables en JavaScript. Se recomienda su uso.
let apellido = "Perez"
console.log(apellido)

//3er método: const. Se utiliza para declarar constantes. No se puede reasignar un valor a una constante.
const PI = 3.1416
console.log(PI)